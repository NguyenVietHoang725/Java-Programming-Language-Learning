public class ifelseStatement {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;

        if (a) {
            System.out.println("a la true");
        } else {
            System.out.println("a la false");
        }

        if (b) {
            System.out.println("b la true");
        } else {
            System.out.println("b la false");
        }
    }
}
