public class IfElseIfLadder {
    public static void main(String[] args) {
        int number = 10;
        
        if (number == 10) {
            System.out.println("Number is 10");
        } else if (number == 20) {
            System.out.println("Number is 20");
        } else {
            System.out.println("Number is not 10 or 20");
        }
    }
}
