public class RelationalOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;
        int c = 6;

        System.out.println("a > b : " + (a > b));
        System.out.println("a < b : " + (a < b));
        System.out.println("a >= b : " + (a >= b));
        System.out.println("a <= b : " + (a <= b));
        System.out.println("a == c : " + (a == c));
        System.out.println("a != c : " + (a != c));
    }
}
